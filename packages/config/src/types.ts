export interface TeamConfig {
  /**
   * The name of the team.
   * @default 'Bit Ocean'
   */
  teamName: string
  /**
   * The URL of the team's GitHub organization.
   * @default 'https://github.com/bit-ocean-studio'
   */
  githubURL: string
  /**
   * The URL of the team's website.
   * @default 'https://www.bit-ocean.studio'
   */
  websiteURL: string
}
